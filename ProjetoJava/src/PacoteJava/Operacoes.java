package PacoteJava;

public class Operacoes {
    
    //método que irá somar os valores
    public static double somaValor(double valor1,double valor2){      
        return (valor1+valor2);
    }
    //método que irá subtrair os valores
    public static double subtraiValor(double valor1, double valor2){
        return (valor1-valor2);        
    }
     //método que irá multiplicar os valores
    public static double multiplicaValor(double valor1, double valor2){
        return (valor1*valor2);
    }
     //método que irá dividir os valores
    public static double divideValor(double valor1, double valor2){
        return (valor1/valor2);
    }   
    
}
