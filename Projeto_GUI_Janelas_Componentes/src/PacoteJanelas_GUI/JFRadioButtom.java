    public void selecionaImagemJButton() {

        ImageIcon img;

        if (rdbPorco.isSelected()) {
            img = new ImageIcon(getClass().getResource("pig.png"));
            img.setImage(img.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), 1));
            lblImagem.setIcon(img);
        }
        if (rdbCoelho.isSelected()) {
            img = new ImageIcon(getClass().getResource("coelho.png"));
            img.setImage(img.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), 1));
            lblImagem.setIcon(img);
        }
        if (rdbGato.isSelected()) {
            img = new ImageIcon(getClass().getResource("cat.png"));
            img.setImage(img.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), 1));
            lblImagem.setIcon(img);
        }
        if (rdbPassaro.isSelected()) {
            img = new ImageIcon(getClass().getResource("bird.png"));
            img.setImage(img.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), 1));
            lblImagem.setIcon(img);
        }
        if (rdbCachorro.isSelected()) {
            img = new ImageIcon(getClass().getResource("dog.png"));
            img.setImage(img.getImage().getScaledInstance(lblImagem.getWidth(), lblImagem.getHeight(), 1));
            lblImagem.setIcon(img);
        }
    }

